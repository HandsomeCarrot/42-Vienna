#include <unistd.h>

int	main(int ac, char **av)
{
	int	pos;

	pos = 0;
	if (ac > 0)
	{
		while (av[0][pos] != '\0')
			write(1, &av[0][pos++], sizeof(char));
		write(1, "\n", sizeof(char));
	}
	return (0);
}
